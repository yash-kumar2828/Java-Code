public class Escape{
    public static void main(String[] args) {
        System.out.println("Hello 'Yash Kumar'");
        System.out.println("Hello \"Yash Kumar\" ");
        System.out.println("Hello \\Yash Kumar\\");
        System.out.println("Hello \nYash Kumar");
        System.out.println("Hello \tYash Kumar");
        System.out.println("Hello \bYash Kumar");
    }
}